task_t * scheduler() {
    // PRIOd scheduler
    task_t *prox = readyQueue;
    if (readyQueue) {
        int alfa = -1, boolean = 0;
        task_t *walk = readyQueue;
        while(walk != readyQueue || !boolean){
			if(walk == readyQueue)
				boolean = !boolean;
			else{
				if(walk->priod <= prox->priod){
					prox->priod += alfa;
					prox = walk;
				}
				else
					walk->priod += alfa;
			}
            walk = walk->next;
        }
        prox->priod = prox->prioe;
		if(PPOS_IS_PREEMPT_ACTIVE)
			prox->taskQuantum = QUANTUM;
    }
    return prox;
}

